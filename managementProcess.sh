| คำสั่ง         | ใช้ทำอะไร               | ตัวอย่าง                  |
| -------------- | ----------------------- | ------------------------- |
| `ps`           | ดู process ปัจจุบัน     | `ps aux`                  |
| `top` / `htop` | ดู CPU/Memory real-time | `top`                     |
| `kill`         | หยุด process            | `kill PID`, `kill -9 PID` |
| `df`           | ดูพื้นที่ disk          | `df -h`                   |
| `du`           | ดูขนาดไฟล์/โฟลเดอร์     | `du -sh folder/`          |
| `free`         | ดู RAM                  | `free -h`                 |
| `uptime`       | ดูเวลาเครื่องเปิด       | `uptime`                  |

# การจัดการกระบวนการและทรัพยากรระบบผ่าน command line
ps
# ดู process ที่กำลังทำงานอยู่ เช่น
# ps aux แสดงรายการ process ทั้งหมดที่กำลังทำงานอยู่ พร้อมรายละเอียดต่างๆ

top
# ดูการใช้งาน CPU และ Memory แบบ real-time เช่น
# top แสดงข้อมูลการใช้งาน CPU, Memory, และ process ที่กำลังทำงานอยู่
# กด q เพื่อออกจากโปรแกรม top

htop
# ดูการใช้งาน CPU และ Memory แบบ real-time ด้วย interface ที่ใช้ง่ายขึ้น เช่น
# htop แสดงข้อมูลการใช้งาน CPU, Memory, และ process ที่กำลังทำงานอยู่ในรูปแบบที่อ่านง่าย
# ใช้ปุ่มลูกศรเพื่อเลื่อนดู process
# กด F10 เพื่อออกจากโปรแกรม htop

kill
# หยุด process ที่กำลังทำงานอยู่ เช่น
# kill PID หยุด process ที่มีหมายเลข PID ที่ระบุ
# kill -9 PID หยุด process ที่มีหมายเลข PID ที่ระบุ โดยบังคับให้หยุดทันที

df
# ดูพื้นที่ว่างในดิสก์ เช่น
# df -h แสดงข้อมูลพื้นที่ดิสก์ในรูปแบบที่อ่านง่าย (human-readable)

du
# ดูขนาดของไฟล์หรือโฟลเดอร์ เช่น
# du -sh folder/ แสดงขนาดรวมของโฟลเดอร์ในรูปแบบที่อ่านง่าย (human-readable)

free
# ดูการใช้งาน RAM เช่น
# free -h แสดงข้อมูลการใช้งาน RAM ในรูปแบบที่อ่านง่าย (human-readable)

uptime
# ดูระยะเวลาที่เครื่องเปิดใช้งาน เช่น
# uptime แสดงระยะเวลาที่เครื่องเปิดใช้งาน พร้อมกับข้อมูลโหลดเฉลี่ย